<template>
	<view class="container">
		<view class="tui-bg"></view>
		<view class="tui-content">
			<view class="tui-form">
				<tui-icon v-if="!payStatus" name="about" margin="30rpx auto" size="130" unit="rpx" color="#eb0909"></tui-icon>
				<tui-loadmore v-if="!payStatus" text="订单支付中..." ></tui-loadmore>

				<tui-icon v-if="payStatus" name="circle-selected" margin="30rpx auto" size="130" unit="rpx" color="#2BB366"></tui-icon>
				<view v-if="payStatus" class="tui-title">支付成功</view>
				<view v-if="payStatus" class="tui-sub-title">非常感谢您购买我们的商品</view>
				<view class="tui-btn-box">
					<tui-button type="danger" width="240rpx" height="70rpx" :size="28" plain shape="circle" @click="$Router.pushTab({name:'index_index'})">返回首页</tui-button>
					<tui-button type="danger" shadow width="240rpx" height="70rpx" :size="28" shape="circle" @click="$Router.push({path:'/pages/order/detail',query:{id:$Route.query.orderid}})">查看订单</tui-button>
				</view>
			</view>
		</view>
		<view class="tui-tips">
			<view class="tui-grey">温馨提示:</view>
			<view class="tui-light-grey">
				付款成功后，本平台不会以付款异常、卡单、系统升级为由联系您。请勿泄露银行卡号、手机验证码，否则会造成钱款损失！谨防电话诈骗！
			</view>
		</view>
	</view>
</template>

<script>
	export default {
		data() {
			return {
				payStatus:false,
			}
		},
		onLoad(){
		    if(!this.$Route.query.orderid){
		        this.$utils.toast('参数错误')
				setTimeout(()=>{this.$Router.back()},2000)
			}else{
		        this.getList()
			}
		},
		methods: {
            getList(){
                let type = this.$Route.query.type?this.$Route.query.type:'order'
                let timeOut = setInterval(()=>{
                    this.$http.get(type+'.pay.result',{id:this.$Route.query.orderid}).then((res)=>{
                        if(res.result) clearInterval(timeOut)
						this.payStatus = res.result
                    })
				},1000)

			},
		}
	}
</script>

